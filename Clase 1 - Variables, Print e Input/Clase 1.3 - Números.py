### Números <int> <float> <complex> ### -------------------------

# Los números en Python están relacionados a los números en matemáticas pero sujetos a las representaciones
# numéricas en las computadoras. Python distingue los números entre tres tipos de numeros:
# Enteros <int>
#   Aquellos que no tienen decimales; tanto positivos como negativos
# Flotante <float>
#   Los números reales que tienen decimales
# Complejos <complex>
#   Son una extensión del sistema numérico que permite la existencia de raíces cuadradas de números negativos

### Operaciones Numéricas en Python ------------------------------

# Los operadores ariméticos son aquellos que nos permiten manejar datos de tipo numérico; permiten realizar operaciones
# matemáticas: suma, resta, multiplicación, división, potencia, etc
# En resultado de una operación aritmética es un dato arimético; si los valores de una operación son enteros, el resultado
# será un número entero, y si uno de los datos es decimal, el resultado será decimal

suma = 3+7
resta = 4-1
multiplicacion = 2*3
potencia = 3**2
division_cociente = 15 / 2 # Nos devuelve un número decimal
division_entero = 15 // 2 # Nos devuelve solo la parte entera del resultado
division_resto = 15 % 2 # Nos devuelve el resto de una división